module.exports = (bot) => {
    bot.onText(/\/help/, (msg) => {
        const helpText = 
            `📖 *কিভাবে ব্যবহার করবেন:*\n\n` +
            `1️⃣ যেই প্ল্যাটফর্ম থেকে ভিডিও ডাউনলোড করতে চান, তার লিংক কপি করুন।\n` +
            `2️⃣ এই বটের চ্যাটে লিংকটি পেস্ট করে পাঠান।\n` +
            `3️⃣ বট ভিডিও প্রসেস করে সরাসরি ভিডিও ফাইল পাঠিয়ে দেবে।\n\n` +
            `⚙️ *কমান্ড সমূহ:*\n` +
            `/start - স্বাগতম বার্তা\n` +
            `/help - এই সাহায্য বার্তা\n\n` +
            `⚠️ বড় ভিডিও ডাউনলোড হতে কিছু সময় লাগতে পারে। ধৈর্য ধরুন।`;
        bot.sendMessage(msg.chat.id, helpText, { parse_mode: 'Markdown' });
    });
};
